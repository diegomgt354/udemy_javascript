

const dia = 0;

switch (dia) { // validacion con el ===, es diferente '2' a 2
    case 0:
        console.log('Domingo');
        break;
    case 1:
        console.log('Lunes');
        break;
    case 2:
        console.log('Martes');
        break;
    case 4:
        console.log('Miercoles');
        break;
    default:
        console.log('Dia incorrecto')
        break;
}

